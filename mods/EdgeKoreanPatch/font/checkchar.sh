#!/usr/bin/bash

# 순회할 문자열 설정
input_string="가간감갖개게경계공과금기끔나너녕노높뉴느는니님다더도동됩뒤드든디딩때래랭레려력로록료를리릭림마막만많머멀메며면명모문뮤받방버벨보북브쁘사새서설세셔셨소속수순쉽스습시십아악안않야어에예오와완요용운원위으은을음이익인임입있자작잘잠재저전점접정제좋주죽중즘지직차체총취커켬코큐크클키킹타터텐통트튼티포표프플하할합해했향화환회효희히"

# 문자열을 한 글자씩 순회
for (( i=0; i<${#input_string}; i++ )); do
    char="${input_string:$i:1}"

    # 현재 폴더의 파일명 중 해당 문자가 포함된 것이 있는지 확인
    # ls 결과에서 문자를 찾고, 검색 결과가 없으면(count가 0이면) 출력
    if ! ls *"$char"* > /dev/null 2>&1; then
        echo "$char"
    fi
done